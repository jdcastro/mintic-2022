#!/usr/bin/env python

# Muestra el dia de la semana según la variable "dia" que se entrega por teclado. 
dia = int(input("Ingresa un número: "))
if dia == 1:
    print("Lunes")
elif dia == 2:
    print("Martes")
elif dia == 3:
    print("Miércoles")
elif dia == 4:
    print("Jueves")
elif dia == 5:
    print("Viernes")
elif dia == 6:
    print("Sábado")
elif dia == 7:
    print("Domingo")
else:
    print("el número no equivale a un día de la semana, escriba un número entre 1 y 7")

